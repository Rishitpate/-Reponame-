package testpackage1;

public class UnaryOperators {
	
	public static void main(String args[]) {
		
		//used with a single variable
		
		int result = +1;
		System.out.println(result);
		
		result--;
		System.out.println(result);
		
		result++;
		System.out.println(result);
		
		result = -result;
		System.out.println(result);
		
		boolean success = true;
		System.out.println(success);
		System.out.println(!success);
	}

}
