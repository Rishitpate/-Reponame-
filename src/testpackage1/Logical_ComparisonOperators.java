package testpackage1;

public class Logical_ComparisonOperators {

	public static void main(String[] args) {
		
		// && Conditional-AND
		// || Conditional-OR
		
		int v1 = 1;
		int v2 = 2;
		
		if((v1==1) && (v2==2)) {
			System.out.println("v1 is equal to 1 and v2 is equal is 2");
		}
		
		if((v1==1) || (v2==1)) {
			System.out.println("v1 is equal to 1 or v2 is equal is 1");
		}

	}

}
